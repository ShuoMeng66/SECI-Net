"""Export experiment summaries to LaTeX tables and a JSON rollup."""
from __future__ import annotations

import argparse
import json
import statistics
from pathlib import Path


LABELS = {
    "bilstm": ("BiLSTM", "BiLSTM"),
    "transformer": ("Transformer", "Transformer"),
    "hybrid": ("Hybrid encoder", "混合编码器"),
    "seci_net_full": ("\\model{} full", "SECI-Net 完整模型"),
    "seci_wo_cf": ("\\model{} w/o CF", "SECI-Net w/o 反事实损失"),
    "seci_wo_gan": ("\\model{} w/o GAN", "SECI-Net w/o GAN"),
    "seci_wo_sparsity": ("\\model{} w/o sparsity", "SECI-Net w/o 稀疏惩罚"),
    "seci_wo_recurrent": ("\\model{} w/o BiLSTM", "SECI-Net w/o 递归分支"),
    "amazon_transformer": ("Transformer", "Transformer"),
    "amazon_hybrid": ("Hybrid encoder", "混合编码器"),
    "amazon_seci_net_full": ("\\model{} full", "SECI-Net 完整模型"),
}


def load_summary(path: Path) -> dict | None:
    summary_path = path / "summary.json"
    if not summary_path.exists():
        return None
    return json.loads(summary_path.read_text(encoding="utf-8"))


def metric_value(summary: dict | None, key: str) -> float | None:
    if summary is None:
        return None
    test_metrics = summary.get("test_metrics") or {}
    return test_metrics.get(key)


def aggregate_runs(paths: list[Path]) -> dict[str, float | str]:
    accs: list[float] = []
    f1s: list[float] = []
    for path in paths:
        summary = load_summary(path)
        acc = metric_value(summary, "accuracy")
        f1 = metric_value(summary, "macro_f1")
        if acc is not None:
            accs.append(acc)
        if f1 is not None:
            f1s.append(f1)
    if not accs:
        return {"accuracy": "--", "macro_f1": "--"}
    if len(accs) == 1:
        return {
            "accuracy": f"{accs[0] * 100:.2f}",
            "macro_f1": f"{f1s[0] * 100:.2f}",
        }
    return {
        "accuracy": f"{statistics.mean(accs) * 100:.2f}$\\pm${statistics.pstdev(accs) * 100:.2f}",
        "macro_f1": f"{statistics.mean(f1s) * 100:.2f}$\\pm${statistics.pstdev(f1s) * 100:.2f}",
    }


def format_cell(value: str) -> str:
    return value if value != "--" else "--"


def write_main_table_en(
    rows: list[tuple[str, dict[str, str]]],
    output: Path,
    *,
    caption: str,
    label: str,
) -> None:
    lines = [
        "% Auto-generated by scripts/export_results.py",
        "\\begin{table}[t]",
        "    \\centering",
        f"    \\caption{{{caption}}}",
        f"    \\label{{{label}}}",
        "    \\small",
        "    \\begin{tabular}{lcc}",
        "    \\toprule",
        "    Model & Accuracy (\\%) & Macro-F1 (\\%) \\\\",
        "    \\midrule",
    ]
    for label_row, metrics in rows:
        lines.append(
            f"    {label_row} & {format_cell(metrics['accuracy'])} & {format_cell(metrics['macro_f1'])} \\\\"
        )
    lines.extend(
        [
            "    \\bottomrule",
            "    \\end{tabular}",
            "\\end{table}",
        ]
    )
    output.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_main_table_cn(
    rows: list[tuple[str, dict[str, str]]],
    output: Path,
    *,
    caption: str,
    label: str,
) -> None:
    lines = [
        "% Auto-generated by scripts/export_results.py",
        "\\begin{table}[htbp]",
        "    \\centering",
        f"    \\caption{{{caption}}}",
        f"    \\label{{{label}}}",
        "    \\small",
        "    \\begin{tabular}{lcc}",
        "    \\hline",
        "    模型 & 准确率 (\\%) & Macro-F1 (\\%) \\\\",
        "    \\hline",
    ]
    for label_row, metrics in rows:
        lines.append(
            f"    {label_row} & {format_cell(metrics['accuracy'])} & {format_cell(metrics['macro_f1'])} \\\\"
        )
    lines.extend(
        [
            "    \\hline",
            "    \\end{tabular}",
            "\\end{table}",
        ]
    )
    output.write_text("\n".join(lines) + "\n", encoding="utf-8")


def collect_group(root: Path, group: str, names: list[str]) -> list[tuple[str, dict[str, str]]]:
    rows: list[tuple[str, dict[str, str]]] = []
    for name in names:
        if group == "ablation":
            base = root / "yelp" / "ablation" / name
        elif group == "amazon":
            base = root / "amazon" / name
        else:
            base = root / "yelp" / name
        paths = sorted(base.glob("seed_*"))
        if not paths:
            paths = [base] if (base / "summary.json").exists() else []
        en_label, _ = LABELS.get(name, (name, name))
        rows.append((en_label, aggregate_runs(paths)))
    return rows


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--checkpoints_root", type=str, default="checkpoints")
    parser.add_argument("--output_dir", type=str, default="tables")
    args = parser.parse_args()

    root = Path(args.checkpoints_root)
    out = Path(args.output_dir)
    out.mkdir(parents=True, exist_ok=True)

    main_names = ["bilstm", "transformer", "hybrid", "seci_net_full"]
    ablation_names = ["seci_wo_cf", "seci_wo_gan", "seci_wo_sparsity", "seci_wo_recurrent"]
    amazon_names = ["amazon_transformer", "amazon_hybrid", "amazon_seci_net_full"]

    main_en = collect_group(root, "main", main_names)
    ablation_en = collect_group(root, "ablation", ablation_names)
    amazon_en = collect_group(root, "amazon", amazon_names)

    write_main_table_en(
        main_en,
        out / "main_results_en.tex",
        caption="Main results on the stratified Yelp fast subset (test set, two seeds).",
        label="tab:main_results",
    )
    write_main_table_en(
        ablation_en,
        out / "ablation_results_en.tex",
        caption="Ablation study on the Yelp fast subset (test set, two seeds).",
        label="tab:ablation",
    )
    write_main_table_en(
        amazon_en,
        out / "amazon_results_en.tex",
        caption="Cross-domain validation on IMDB (single seed, external validation).",
        label="tab:amazon",
    )

    main_cn = [(LABELS[n][1], m) for n, m in zip(main_names, [row[1] for row in main_en])]
    ablation_cn = [(LABELS[n][1], m) for n, m in zip(ablation_names, [row[1] for row in ablation_en])]
    write_main_table_cn(
        main_cn,
        out / "main_results_cn.tex",
        caption="Yelp 主实验结果（测试集，\\%）",
        label="tab:main_result",
    )
    write_main_table_cn(
        ablation_cn,
        out / "ablation_results_cn.tex",
        caption="SECI-Net 消融实验（测试集，\\%）",
        label="tab:ablation",
    )

    rollup = {
        "main": {name: metrics for name, metrics in zip(main_names, [m for _, m in main_en])},
        "ablation": {name: metrics for name, metrics in zip(ablation_names, [m for _, m in ablation_en])},
        "amazon": {name: metrics for name, metrics in zip(amazon_names, [m for _, m in amazon_en])},
    }
    (out / "results_rollup.json").write_text(
        json.dumps(rollup, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"Exported tables to {out}")


if __name__ == "__main__":
    main()
